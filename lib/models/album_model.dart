class AlbumModel {
  final num id;
  final String image;
  final int songs;
  final String year;
  final String title;
  final String artist;
  final num plays;
  final num download;
  final String genre;
  final num like;

  AlbumModel({
    required this.id,
    required this.image,
    required this.songs,
    required this.year,
    required this.title,
    required this.artist,
    required this.plays,
    required this.download,
    required this.genre,
    required this.like,
  });
}
